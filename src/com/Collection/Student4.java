package com.Collection;

public class Student4 {

	 int sid ; 
	 String name;
	 String course;
	public Student4(int sid, String name, String course) {
		super();
		this.sid = sid;
		this.name = name;
		this.course = course;
	}
	@Override
	public String toString() {
		return "Student4 [sid=" + sid + ", name=" + name + ", course=" + course + "]";
	}
	
}
